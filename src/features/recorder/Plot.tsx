import type { FitResult, MotionPoint } from "../../domain/types";

type PlotProps = {
  points: MotionPoint[];
  fit?: FitResult;
};

export function Plot({ points, fit }: PlotProps) {
  if (points.length === 0) {
    return <div className="empty-plot">No position samples yet.</div>;
  }

  const axis = chooseAxis(points);
  const values = points.map((point) =>
    axis === "x" ? point.xMeters : point.yMeters,
  );
  const fittedValues = fit?.fitted.map((point) => point.value) ?? [];
  const allT = [
    ...points.map((point) => point.t),
    ...(fit?.fitted.map((point) => point.t) ?? []),
  ];
  const allY = [...values, ...fittedValues];
  const domain = extent(allT);
  const range = paddedExtent(allY);
  const width = 760;
  const height = 320;
  const pad = { left: 54, right: 18, top: 18, bottom: 42 };
  const innerWidth = width - pad.left - pad.right;
  const innerHeight = height - pad.top - pad.bottom;
  const xScale = (value: number) =>
    pad.left +
    ((value - domain[0]) / (domain[1] - domain[0] || 1)) * innerWidth;
  const yScale = (value: number) =>
    pad.top +
    innerHeight -
    ((value - range[0]) / (range[1] - range[0] || 1)) * innerHeight;
  const fitPath =
    fit && fit.fitted.length > 0
      ? fit.fitted
          .map(
            (point, index) =>
              `${index === 0 ? "M" : "L"} ${xScale(point.t).toFixed(2)} ${yScale(point.value).toFixed(2)}`,
          )
          .join(" ")
      : "";

  return (
    <figure className="plot-shell">
      <svg
        viewBox={`0 0 ${width} ${height}`}
        role="img"
        aria-label={`${axis} position over time`}
      >
        <line
          className="axis"
          x1={pad.left}
          y1={pad.top}
          x2={pad.left}
          y2={height - pad.bottom}
        />
        <line
          className="axis"
          x1={pad.left}
          y1={height - pad.bottom}
          x2={width - pad.right}
          y2={height - pad.bottom}
        />
        {[0, 0.25, 0.5, 0.75, 1].map((tick) => {
          const x = pad.left + tick * innerWidth;
          const label = domain[0] + tick * (domain[1] - domain[0]);
          return (
            <g key={`x-${tick}`}>
              <line
                className="grid"
                x1={x}
                y1={pad.top}
                x2={x}
                y2={height - pad.bottom}
              />
              <text className="tick" x={x} y={height - 14} textAnchor="middle">
                {label.toFixed(1)}s
              </text>
            </g>
          );
        })}
        {[0, 0.5, 1].map((tick) => {
          const y = pad.top + tick * innerHeight;
          const label = range[1] - tick * (range[1] - range[0]);
          return (
            <g key={`y-${tick}`}>
              <line
                className="grid"
                x1={pad.left}
                y1={y}
                x2={width - pad.right}
                y2={y}
              />
              <text className="tick" x={8} y={y + 4}>
                {label.toFixed(2)}m
              </text>
            </g>
          );
        })}
        {fitPath ? <path className="fit-line" d={fitPath} /> : null}
        {points.map((point) => {
          const value = axis === "x" ? point.xMeters : point.yMeters;
          const sampleClass = point.issues.includes("position-outlier")
            ? "sample outlier"
            : point.confidence < 0.5
              ? "sample low"
              : "sample";
          return (
            <circle
              key={point.id}
              className={sampleClass}
              cx={xScale(point.t)}
              cy={yScale(value)}
              r={
                sampleClass.includes("outlier")
                  ? 5
                  : point.confidence < 0.5
                    ? 3.8
                    : 3
              }
            />
          );
        })}
      </svg>
      <figcaption>
        Plotting {axis}-axis position because it has the strongest motion
        signal.
      </figcaption>
      {fit?.plotPng ? (
        <details className="scipy-plot">
          <summary>Matplotlib plot from Pyodide/SciPy</summary>
          <img src={fit.plotPng} alt="Matplotlib chart generated by Pyodide" />
        </details>
      ) : null}
    </figure>
  );
}

function chooseAxis(points: MotionPoint[]) {
  const xRange = extent(points.map((point) => point.xMeters));
  const yRange = extent(points.map((point) => point.yMeters));
  return xRange[1] - xRange[0] >= yRange[1] - yRange[0] ? "x" : "y";
}

function extent(values: number[]) {
  return [Math.min(...values), Math.max(...values)] as const;
}

function paddedExtent(values: number[]) {
  const [min, max] = extent(values);
  const padding = Math.max(0.01, (max - min) * 0.12);
  return [min - padding, max + padding] as const;
}
